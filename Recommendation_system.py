# --- 2. Function to Build User Profile Vector (Unchanged) ---

def build_user_profile(genre_list, all_genres):
    """
    Creates a numerical vector (1s and 0s) representing the user's preferred genres.
    """
    user_vector = np.zeros(len(all_genres))
    
    for i, genre in enumerate(all_genres):
        if genre.strip().lower() in [g.strip().lower() for g in genre_list]:
            user_vector[i] = 1
            
    # Reshape for use with cosine_similarity: (1 row, N features)
    return user_vector.reshape(1, -1)

# --- 3. Recommendation Function (ML Logic - Unchanged) ---

def get_genre_list_recommendations(user_genres, movies_df, all_genres, n=3):
    """
    Recommends movies by comparing the user's genre profile to all movie genre vectors.
    """
    # 1. Build the User Profile Vector
    user_vector = build_user_profile(user_genres, all_genres)
    
    # Check if any valid genres were entered
    if np.sum(user_vector) == 0:
        # A more helpful error message in a large dataset
        return " Error: None of the entered genres were recognized. Please check your spelling against the available list."

    # 2. Extract Movie Feature Matrix
    movie_vectors = movies_df.values
    
    # 3. Calculate Similarity (User-to-Item)
    # Cosine Similarity is run between the user vector and the movie matrix (1x20) @ (100x20)
    similarity_scores = cosine_similarity(user_vector, movie_vectors)
    
    
    # 4. Convert scores to a Pandas Series and sort
    scores_series = pd.Series(similarity_scores[0], index=movies_df.index)
    
    # Sort and return top N recommendations
    recommendations = scores_series.sort_values(ascending=False)
    
    return recommendations.head(n)

